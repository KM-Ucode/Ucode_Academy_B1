package day_02_introduction_to_java;

public class Variables {
    public static void main(String[] args) {
        // Variable naming
        // Letters, numbers, _, $
        // Cannot start with numbers
        // Recommendation: follow camel case

        // Variable declaration

        byte myByte;
        int myAge = 25;
        double myDouble = 5.25;
        float myFloat = 3.4F;
        long myLong = 2222L;

        int _d;
        int $test;


                //Assignment
        myByte = 10;
        _d =14;
        $test = 45;

        //char
        char ch = 'a';

        // boolean
        boolean isTrue = true;
        boolean isWeatherNice;
        isWeatherNice = false;

    }
}
