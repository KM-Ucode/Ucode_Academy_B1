package short_videos;

public class RelationalOperators {
    public static void main(String[] args) {

        // All relational operators return boolean(true or false)

        // > Greater than
        System.out.println(5>1);   // it returns true
        System.out.println(5>10);  // it returns false

        // >= Greater than or equal

        System.out.println(5 >= 5);  // it returns true
        System.out.println(5 >= 50); // false

        // < Less than

        System.out.println(5 <10); // true
        System.out.println(5<1);   // false

        // <= Less than or equal

        System.out.println(5 <= 10);  // true
        System.out.println(5 <= 5);  // true

        // == Equal
        System.out.println(5 == 6); // false
        System.out.println(5 == 5); // true

        // != Not equal

        System.out.println(5 != 5); // false
        System.out.println(5 != 4); // true




    }
}
