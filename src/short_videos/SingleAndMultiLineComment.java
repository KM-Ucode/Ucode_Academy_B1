package short_videos;

public class SingleAndMultiLineComment {
    public static void main(String[] args) {


        // Single line comment

        //Multi line comment
        /*
        sjdfgkjehflkdjlgkjldjgjkfg
         */
        // println() vs print()

       // System.out.println();  : Appends a new line at the end of data output
        System.out.println("Hello there");
        System.out.println("Hello again");

       // System.out.print();  :   Does not append a new line at the end of data output
        System.out.print("Hello there");
        System.out.print("Hello again");


    }
}
