package day_05_unary_shorthand_operators;

public class ShorthandOperatorsShortVideo {
    public static void main(String[] args) {

        // Assignment
        int num = 10;

        // Addition Assignment
        //num = num + 10;
        num += 10;

        // Subtraction Assignment
        //num = num - 5;
        num -= 5;

        // Multiplication Assignment
        // num = num * 5;
        num *= 5;

        // Division Assignment
        //  num = num / 5;
        num /= 5;

        // Remainder Assignment
        // num = num % 2;
        num %= 2;
    }
}
